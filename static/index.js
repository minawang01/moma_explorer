$(document).ready(function() {
    for (let elem of pop_art) {
        console.log(elem)
        let art_ctn = $("<div class='art_ctn row flex-row mt-1 mb-1'>")
        let img_ctn = $("<div class='col-md-6 img-ctn'>")
        let img = $("<img class='img-fluid'>")
        let img_link = $("<a>")
        img_link.attr("href", window.location.origin + "/view/" + elem["id"])
        img.attr("src", elem["img"])
        img.attr("alt", elem["alt_text"])
        img_link.append(img)
        img_ctn.append(img_link)
        let desc = $("<div class='col-md-6 desc'>")
        let title = $("<span class='mini-title font base'>")
        let link = $("<a class='base'>")
        link.attr("href", window.location.origin + "/view/" + elem["id"])
        link.text(elem["title"])
        title.append(link)
        let artist = $("<span class='mini-artist font'>")
        artist.text(elem["artist"])
        let sentence = $("<span class='font mini-loc'>On view at </span>")
        if (elem["gallery"]) {
            let gall = $("<span class='accent'>")
            gall.text(elem["gallery"])
            sentence.append(gall)
            sentence.append(" on ")
            let floor = $("<span class='accent'>")
            floor.text("Floor " + elem["floor"])
            sentence.append(floor)
        } else {
            let floor = $("<span class='accent'>")
            floor.text("Floor " + elem["floor"])
            sentence.append(floor)
        }
        desc.append(title)
        desc.append("<br>")
        desc.append(artist)
        desc.append("<br>")
        desc.append(sentence)
        art_ctn.append(img_ctn)
        art_ctn.append(desc)
        $(".popular_artworks").append(art_ctn)
    }
})